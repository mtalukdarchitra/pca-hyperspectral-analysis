# Utilities
def load_data(cube_path):
    cube = np.load(cube_path).astype(np.float64)
    h, w, b = cube.shape
    X = cube.reshape(-1, b)
    return cube, X, h, w, b


def mean_center(X):
    mu = X.mean(axis=0, keepdims=True)
    Xc = X - mu
    return Xc, mu


def reconstruction_error(X_true, X_hat):
    return np.linalg.norm(X_true - X_hat, ord="fro") / (
        np.linalg.norm(X_true, ord="fro") + 1e-12
    )


def compression_ratio(n, d, k):
    return (n * d) / (k * (n + d))


def cumulative_explained_variance(eigvals):
    eigvals = np.maximum(eigvals, 0)
    evr = eigvals / (eigvals.sum() + 1e-12)
    return np.cumsum(evr)


# Method 1: PCA via EVD
def pca_evd(Xc, k_list):
    n, d = Xc.shape

    t0 = time.perf_counter()
    C = (Xc.T @ Xc) / (n - 1)
    eigvals, eigvecs = np.linalg.eigh(C)

    idx = np.argsort(eigvals)[::-1]
    eigvals = eigvals[idx]
    eigvecs = eigvecs[:, idx]

    total_time = time.perf_counter() - t0

    results = {}

    for k in k_list:
        W = eigvecs[:, :k]
        Z = Xc @ W
        X_hat = Z @ W.T

        err = reconstruction_error(Xc, X_hat)

        flops = 2 * n * d * d + d**3 + 4 * n * d * k

        results[k] = {
            "error": err,
            "runtime": total_time,
            "flops": flops,
            "eigvals": eigvals
        }

    return results


# Method 2: PCA via SVD
def pca_svd(Xc, k_list):
    n, d = Xc.shape

    t0 = time.perf_counter()
    U, S, Vt = np.linalg.svd(Xc, full_matrices=False)
    total_time = time.perf_counter() - t0

    explained_variance = (S**2) / (n - 1)

    results = {}

    for k in k_list:
        Uk = U[:, :k]
        Sk = S[:k]
        Vtk = Vt[:k, :]

        X_hat = (Uk * Sk) @ Vtk

        err = reconstruction_error(Xc, X_hat)

        flops = 2 * n * d * d + 11 * d**3 + 2 * n * k * d

        results[k] = {
            "error": err,
            "runtime": total_time,
            "flops": flops,
            "eigvals": explained_variance
        }

    return results


# Method 3: PCA via Power Iteration
def top_eigenpair_power(A, max_iters=300, tol=1e-8, rng=None):
    d = A.shape[0]

    x = rng.normal(size=d)
    x = x / (np.linalg.norm(x) + 1e-12)

    num_iters = 0

    for _ in range(max_iters):
        y = A @ x
        y_norm = np.linalg.norm(y) + 1e-12
        x_new = y / y_norm

        num_iters += 1

        if np.linalg.norm(x_new - x) < tol:
            x = x_new
            break

        x = x_new

    lam = float(x.T @ A @ x)

    return lam, x, num_iters


def pca_power_iteration(Xc, k_list, max_iters=300, tol=1e-8, seed=7):
    n, d = Xc.shape
    rng = np.random.default_rng(seed)

    t0 = time.perf_counter()

    C = (Xc.T @ Xc) / (n - 1)
    A = C.copy()

    eigvals = []
    eigvecs = []
    total_iters = 0

    max_k = max(k_list)

    for _ in range(max_k):
        lam, v, iters_used = top_eigenpair_power(
            A,
            max_iters=max_iters,
            tol=tol,
            rng=rng
        )

        eigvals.append(lam)
        eigvecs.append(v)
        total_iters += iters_used

        A = A - lam * np.outer(v, v)

    eigvals = np.array(eigvals)
    eigvecs = np.column_stack(eigvecs)

    total_time = time.perf_counter() - t0

    results = {}

    for k in k_list:
        W = eigvecs[:, :k]
        Z = Xc @ W
        X_hat = Z @ W.T

        err = reconstruction_error(Xc, X_hat)

        avg_iters = max(1, total_iters / max_k)

        flops = (
            2 * n * d * d
            + k * (2 * d * d * avg_iters + d * d)
            + 4 * n * d * k
        )

        results[k] = {
            "error": err,
            "runtime": total_time,
            "flops": flops,
            "eigvals": eigvals
        }

    return results


