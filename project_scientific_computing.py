# Extra diagnostics for Power Iteration:
# 1) Iteration count per extracted component
# 2) Residual convergence history
#This behavior is consistent with high-dimensional factor models,
#where weak factors lead to slower convergence of iterative PCA
#methods
#In practice, this can lead to apparent non-convergence within
#a fixed iteration budget, as observed in this experiment.

POWER_DIAG_K = 10


def top_eigenpair_power_with_history(A, max_iters=300, tol=1e-8, rng=None):
    d = A.shape[0]

    if rng is None:
        rng = np.random.default_rng(7)

    x = rng.normal(size=d)
    x = x / (np.linalg.norm(x) + 1e-12)

    iterate_diffs = []
    residuals = []

    A_norm = np.linalg.norm(A, ord="fro") + 1e-12

    for _ in range(max_iters):
        y = A @ x
        y_norm = np.linalg.norm(y) + 1e-12
        x_new = y / y_norm

        lam_new = float(x_new.T @ A @ x_new)

        iterate_diff = np.linalg.norm(x_new - x)
        residual = np.linalg.norm(A @ x_new - lam_new * x_new) / A_norm

        iterate_diffs.append(iterate_diff)
        residuals.append(residual)

        x = x_new

        if iterate_diff < tol:
            break

    lam = float(x.T @ A @ x)

    return lam, x, len(iterate_diffs), iterate_diffs, residuals


def power_iteration_diagnostics(Xc, k=10, max_iters=300, tol=1e-8, seed=7):
    n, d = Xc.shape
    rng = np.random.default_rng(seed)

    C = (Xc.T @ Xc) / (n - 1)
    A = C.copy()

    eigvals = []
    iteration_counts = []
    iterate_diff_histories = []
    residual_histories = []

    for component in range(1, k + 1):
        lam, v, iters, diffs, residuals = top_eigenpair_power_with_history(
            A,
            max_iters=max_iters,
            tol=tol,
            rng=rng
        )

        eigvals.append(lam)
        iteration_counts.append(iters)
        iterate_diff_histories.append(diffs)
        residual_histories.append(residuals)

        A = A - lam * np.outer(v, v)

    diagnostics = {
        "eigvals": np.array(eigvals),
        "iteration_counts": iteration_counts,
        "iterate_diff_histories": iterate_diff_histories,
        "residual_histories": residual_histories
    }

    return diagnostics


